#include <stdio.h>

int main() {
    int rows, columns;
    scanf("%d", &rows);
    scanf("%d", &columns);
    
    // Write your code here to print the pattern
    for(int i=1;i<=rows;i++){
        for(int j=1;j<=columns;j++){
            printf("*");
        }
        printf("\n");
    }
    return 0;
}
